package IntroducaoAoJava.TiposPrimitivos;

public class TiposPrimitivos {
    public static void main(String[] args) {
        byte num1 = 127; // 8 bits
        short num2 = 32767; // 16 bits
        int num3 = 22222; // 32 bits
        Long num4 = Long.MAX_VALUE; // 64 bits
        float num5 = Float.MAX_VALUE; // 32 bits
        double num6 = Double.MAX_VALUE; // 64 bits
        char caracter = 'a'; // unicode
        boolean estado = true; // true or false

        System.out.println("num1 = " + num1);
        System.out.println("num1 = " + num2);
        System.out.println("num1 = " + num3);
        System.out.println("num1 = " + num4);
        System.out.println("num1 = " + num5);
        System.out.println("num1 = " + num6);
        System.out.println("num1 = " + caracter);
        System.out.println("num1 = " + estado);
    }
}
